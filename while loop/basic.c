//print 1 to 100 numbers using while loop
// In while loop initialization is written before and upgradation is written afterwards
//while loop ka use tab karte h jab hume jyada cheezein naa pata ho
#include <stdio.h>
int main()
{
    int i=1;
    while (i<101)
    {
        printf("%d ",i);
        i++;
    }
    return 0;
}